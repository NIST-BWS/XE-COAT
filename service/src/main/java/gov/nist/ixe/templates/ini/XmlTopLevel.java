package gov.nist.ixe.templates.ini;

import java.util.ArrayList;
import java.util.List;

public class XmlTopLevel extends XmlFragment {
	protected List<XmlSection> sectionTypes = new ArrayList<XmlSection>();
	
	public List<XmlSection> getSectionTypes() {
		return sectionTypes; 
	}
	
	public void addSectionType(XmlSection sectionType) { 
		sectionTypes.add(sectionType); 
	}
}